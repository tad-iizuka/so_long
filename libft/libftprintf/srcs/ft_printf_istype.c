#include	"ft_printf.h"

int	ft_printf_istype(int c)
{
	return (\
		c == 'd' || c == 'i' || c == 'u' || \
		c == 'x' || c == 'X' || c == 'o' || \
		c == 's' || c == 'c' || c == 'p');
}
